export function getFaqByTags(tags?: PamporovoFaqTag[]): PamporovoFaqItem[] {
  if (!tags?.length) return PAMPOROVO_FAQ;
  return PAMPOROVO_FAQ.filter((item) => item.tags.some((t) => tags.includes(t)));
}

export function faqToSchema(items: PamporovoFaqItem[]): Record<string, unknown> {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: items.map((item) => ({
      "@type": "Question",
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  };
}
